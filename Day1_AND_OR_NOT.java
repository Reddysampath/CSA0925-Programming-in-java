import java.util.*;
class Day1_AND_OR_NOT
{
public static void main(String arg[])
{
int a=15,b=8;
if(a>12 && b<10)
{
System.out.println("true"); 
}
else
{
System.out.println("false"); 
}
if(a<10 || b<10)
{
System.out.println("true"); 
}
else
{
System.out.println("false"); 
}
if(a!=b)
{
System.out.println("false"); 
}
else
{
System.out.println("true");
}
}
}